"""
定义函数,在终端中打印一维列表.
"""


def print_list(num_list):
    for item in num_list:
        print(item)


list01 = [5, 546, 6, 56, 76, ]
list02 = [7, 6, 879, 9, 909, ]

print_list(list01)
print_list(list02)
